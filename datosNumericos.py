print("Python tiene tres tipos numéricos: int, float y complex")
print("1. int")
miValor=1
print(miValor)
print(type(miValor))
print(str(miValor) + " es del tipo de datos " + str(type(miValor)))

print("2. float")
miValor=3.14
print(miValor)
print(type(miValor))
print(str(miValor) + " es del tipo de datos " + str(type(miValor)))

print("3. complex")
miValor=5j
print(miValor)
print(type(miValor))
print(str(miValor) + " es del tipo de datos " + str(type(miValor)))

